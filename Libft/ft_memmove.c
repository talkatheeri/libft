#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t num)
{
	char	*d;
	char	*s;

	d = (char *)dest;
	s = (char *)src;
	if (dest == src)
		return (dest);
	if (s < d)
	{
		while (num--)
			*(d + num) = *(s + num);
		return (dest);
	}
	while (num--)
		*d++ = *s++;
	return (dest);
}
